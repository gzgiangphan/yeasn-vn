const langToggle = document.querySelector("[data-lang-toggle]");
const currentLang = document.querySelector("[data-lang-current]");
const nextLang = document.querySelector("[data-lang-next]");
const langStorage = {
  get() {
    try {
      return localStorage.getItem("neosee-lang");
    } catch {
      return null;
    }
  },
  set(lang) {
    try {
      localStorage.setItem("neosee-lang", lang);
    } catch {
      // The toggle still works for the current page even if storage is unavailable.
    }
  },
};
const storedLang = langStorage.get();
let activeLang = storedLang === "en" ? "en" : "vi";

function applyLanguage(lang) {
  const copy = translations[lang];
  document.documentElement.lang = lang;
  document.title = copy.title;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (copy[key]) {
      element.innerHTML = copy[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.dataset.i18nAria;
    if (copy[key]) {
      element.setAttribute("aria-label", copy[key]);
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (copy[key]) {
      element.setAttribute("alt", copy[key]);
    }
  });

  currentLang.textContent = lang === "vi" ? "VI" : "EN";
  nextLang.textContent = lang === "vi" ? "EN" : "VI";
  langToggle.setAttribute("aria-label", copy.switchLabel);
  langStorage.set(lang);
}

langToggle.addEventListener("click", () => {
  activeLang = activeLang === "vi" ? "en" : "vi";
  applyLanguage(activeLang);
});

applyLanguage(activeLang);
